package pe.gob.ep.iclassq.entidad;

public class BeanUsuariosPermiso {
	private Integer idUsuario;
	private String ejecutivo;
	private String usuario;
	private String zona;
	private String oficina;
	private String area;
	private String ventanilla;
	private String atiendeManual;
	public Integer getIdUsuario() {
		return idUsuario;
	}
	public void setIdUsuario(Integer idUsuario) {
		this.idUsuario = idUsuario;
	}
	public String getEjecutivo() {
		return ejecutivo;
	}
	public void setEjecutivo(String ejecutivo) {
		this.ejecutivo = ejecutivo;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getZona() {
		return zona;
	}
	public void setZona(String zona) {
		this.zona = zona;
	}
	public String getOficina() {
		return oficina;
	}
	public void setOficina(String oficina) {
		this.oficina = oficina;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getVentanilla() {
		return ventanilla;
	}
	public void setVentanilla(String ventanilla) {
		this.ventanilla = ventanilla;
	}
	public String getAtiendeManual() {
		return atiendeManual;
	}
	public void setAtiendeManual(String atiendeManual) {
		this.atiendeManual = atiendeManual;
	}
}
